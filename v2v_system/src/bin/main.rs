static PACKET_RECEIVED: Channel<CriticalSectionRawMutex, Message, 8> = Channel::new();
static PACKET_TO_SEND: Channel<CriticalSectionRawMutex, Message, 8> = Channel::new();
static PACKET_TO_ANALYZE: Channel<CriticalSectionRawMutex, Message, 16> = Channel::new();
const CAR_ID: u16 = 0x05;
type Waypoint = (f64, f64, f64, f64, i32);

fn make_key(car_id: u16, packet_id: u16) -> u32 {
    (car_id as u32) << 16 | (packet_id as u32)
}

#[embassy_executor::task]
async fn packet_processer() {
    loop {
        let packet = PACKET_TO_ANALYZE.receive().await;
        println!("Received a Packet: {packet:?}");
    }
}

#[embassy_executor::task]
async fn packet_receiver(mut esp_now_receiver: EspNowReceiver<'static>, own_mac: [u8; 6]) {
    loop {
        let received_packet = esp_now_receiver.receive_async().await;
        let sender = received_packet.info.src_address;
        println!("sender mac: {sender:?}");
        println!("my mac: {own_mac:?}");
        if sender == own_mac {
            continue;
        }
        if let Ok(packet) = from_bytes::<Message>(received_packet.data()) {
            if let Err(e) = PACKET_RECEIVED.try_send(packet) {
                println!("Receiver queue full, dropping packet: {:?}", e);
            }
        }
    }
}

#[embassy_executor::task]
async fn packet_sender(mut esp_now_sender: EspNowSender<'static>) {
    let mut rebrodcasted_packets = heapless::Vec::<u32, 50>::new();
    loop {
        let packet = select(PACKET_RECEIVED.receive(), PACKET_TO_SEND.receive()).await;
        match packet {
            Either::First(message) => match message {
                Message::Position(data) => {
                    let key = make_key(data.car_id, data.packet_id);
                    if rebrodcasted_packets.contains(&key) {
                        continue;
                    }
                    const SIZE: usize = Message::POSTCARD_MAX_SIZE;
                    let message = Message::Position(data);
                    match to_vec::<Message, SIZE>(&message) {
                        Ok(payload) => {
                            if let Err(e) = esp_now_sender.send(&BROADCAST_ADDRESS, payload.deref())
                            {
                                println!("Send failed: {:?}", e);
                            } else {
                                if rebrodcasted_packets.len() == rebrodcasted_packets.capacity() {
                                    rebrodcasted_packets.remove(0);
                                }
                                if let Err(e) = rebrodcasted_packets.push(key) {
                                    println!("Rebroadcast cache full, discarding key: {:?}", e);
                                }
                                if let Err(e) = PACKET_TO_ANALYZE.try_send(Message::Position(data))
                                {
                                    println!("Analysis queue full, dropping packet: {:?}", e);
                                }
                            }
                        }
                        Err(e) => {
                            println!("Serialization failed: {:?}", e);
                        }
                    }
                }
            },
            Either::Second(message) => {
                const SIZE: usize = Message::POSTCARD_MAX_SIZE;

                let Message::Position(data) = message;
                let key = make_key(data.car_id, data.packet_id);

                match to_vec::<Message, SIZE>(&message) {
                    Ok(payload) => {
                        println!("TX {}", key);
                        if let Err(e) = esp_now_sender.send(&BROADCAST_ADDRESS, payload.deref()) {
                            println!("Send failed: {:?}", e);
                        } else {
                            if rebrodcasted_packets.len() == rebrodcasted_packets.capacity() {
                                rebrodcasted_packets.remove(0);
                            }
                            if let Err(e) = rebrodcasted_packets.push(key) {
                                println!("Rebroadcast cache full, discarding key: {:?}", e);
                            }
                        }
                    }
                    Err(e) => {
                        println!("Serialization failed: {:?}", e);
                    }
                }
            }
        }
    }
}

#[embassy_executor::task]
async fn gps_sender(waypoints: [Waypoint; 304]) {
    Timer::after(Duration::from_secs(10)).await;
    let mut seq: u16 = 0;
    let mut index: usize = 0;
    loop {
        let pkt_id = seq;
        seq = seq.wrapping_add(1);
        let (lat, lon, alt, spd, hdg) = waypoints[index];
        index = (index + 1) % waypoints.len();
        let position_messgae = PositionMessage::new(
            CAR_ID, lat as f32, lon as f32, hdg as f32, spd as f32, alt as f32, pkt_id,
        );
        let packet = Message::Position(position_messgae);
        match PACKET_TO_SEND.try_send(packet) {
            Ok(_) => println!("Queued {}", pkt_id),
            Err(e) => println!("Dropped {}: {:?}", pkt_id, e),
        }
        Timer::after(Duration::from_secs(1)).await;
    }
}

